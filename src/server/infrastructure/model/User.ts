import * as mongoose from "mongoose";
import {Model, Schema} from "mongoose"

const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now()
    }
});

export const User = mongoose.model('users', UserSchema);